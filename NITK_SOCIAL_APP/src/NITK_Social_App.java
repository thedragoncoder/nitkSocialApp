    private void profileFrameWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_profileFrameWindowActivated
        // TODO add your handling code here:
        makeConnection();
        try{
            profileUserIDLabel.setText(userToView);
            String sql = "SELECT * FROM Users WHERE UserID = '" + userToView + "';";
            rs = stmt.executeQuery(sql);
            if(rs.first()){
                profileNameTF.setText(rs.getString("Name"));
                profileEmailIDTF.setText(rs.getString("EmailID"));
                profileWebsiteLinkTF.setText(rs.getString("Website"));
            }
            else{
                sql = "SELECT * FROM Organization WHERE OrgID = '" + userToView + "';";
                rs = stmt.executeQuery(sql);
                rs.first();
                profileNameTF.setText(rs.getString("Name"));
                profileEmailIDTF.setText(rs.getString("EmailID"));
                profileWebsiteLinkTF.setText(rs.getString("Website"));
            }
            if(!userToView.equals(USER)){
                profileNameTF.setEditable(false);
                profileEmailIDTF.setEditable(false);
                profileWebsiteLinkTF.setEditable(false);
                profileUpdateButton.setVisible(false);
            }
            else{
                profileNameTF.setEditable(true);
                profileEmailIDTF.setEditable(true);
                profileWebsiteLinkTF.setEditable(true);
                profileUpdateButton.setVisible(true);
            }
            breakConnection();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
            breakConnection();
        }   
        
        
    }//GEN-LAST:event_profileFrameWindowActivated

